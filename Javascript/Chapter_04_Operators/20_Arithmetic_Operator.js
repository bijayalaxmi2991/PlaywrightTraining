//Operator - A symbol that performs an operation on operands
//Operand - On which the operation is performed

//Arithmetic operators - used to perform arithmetic operations on numbers

let a=10, b=5;
console.log(a); // Output: 10
console.log(b); // Output: 5

//Arithmatic operators - '+', '-', '*', '/', '%', '**'
console.log(a+b); // Output: 15
console.log(a-b); // Output: 5
console.log(a*b); // Output: 50
console.log(a/b); // Output: 2
console.log(a%b); // Output: 0, remainder operator, gives the remainder after division
console.log(a**b); // Output: 100000, power operator, 10 raised to the power of 5

//Increament and Decrement operators
// ++ : Increament operator, adds 1 to the variable
// -- : Decrement operator, subtracts 1 from the variable

console.log(a++); // Output: 10, post-increment, returns the value before incrementing
console.log(a); // Output: 11, now a is incremented to 11

console.log(++b); // Output: 6, pre-increment, increments the value first and then returns it
console.log(b); // Output: 6, b is now 6


