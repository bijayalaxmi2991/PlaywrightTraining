//Comparison operators - used to compare two values and return a boolean result
// ==, ===, !=, !==, >, <, >=, <=
console.log(3>4); // Output: false
console.log(3<4); // Output: true
console.log(4>=4); // Output: true : 4 > 4 or 4 = 4

console.log(5=="5"); // Output: true : 5 is equal to 5
console.log(5==="5"); // Output: false : 5 is not strictly equal to 5 (different types)
console.log(5!="5"); // Output: false : 5 is not equal to 5
console.log(5!=="5"); // Output: true : 5 is not strictly equal to 5 (different types)
