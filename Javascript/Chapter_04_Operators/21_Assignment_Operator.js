// += : x = x+10;
// -= : x = x-10;
// *= : x = x*10;
// /= : x = x/10;
// %= : x = x%10;


let x = 10;
x += 5; // x = x + 5
console.log(x); // Output: 15
x -= 3; // x = x - 3
console.log(x); // Output: 12
x *= 2; // x = x * 2
console.log(x); // Output: 24
x /= 4; // x = x / 4
console.log(x); // Output: 6
x %= 3; // x = x % 3
console.log(x); // Output: 0